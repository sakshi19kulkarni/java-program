package firstprogram;

public class DataTypeProg {
	public static void main(String args[]) {
	int a = 10;
	char b = 'A';
	double c = 10.12;
	float d = 12.15f;
	
	System.out.println("value of integer is:"+a);
	System.out.println("value of character is:"+b);
	System.out.println("value of double is:"+c);
	System.out.println("value of float is:"+d);
	
	
			

}
}
